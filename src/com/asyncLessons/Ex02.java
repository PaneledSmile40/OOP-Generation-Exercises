package com.asyncLessons;

public class Ex02 {

	public static void main(String[] args) {
		int a = 10;
		int b = 4;
		int c = 3;
		
		System.out.println("le mie variabili sono a  = " + a + ", b = " + b + ", c = " + c);
		System.out.println("il risultato di [a + b * c]  è: " + (a + b * c));
		System.out.println("il risultato di [(a + b) * c]  è: " + ((a + b) * c));
		System.out.println("il risultato di [a / b + c]  è: " + (a / b + c));
		System.out.println("il risultato di [a % b]  è: " + (a % b));
	}

}
